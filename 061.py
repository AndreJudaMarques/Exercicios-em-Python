''' termo e razao com while, mostrar 10'''

termo = int(input('Digite o primeiro termo: '))

razao = int(input('Digite a razao: '))

cont = 0

while cont < 9:
    print(termo, end = ' ')
    while cont < 9:
        termo = termo + razao
        cont += 1
        print(termo, end=' ')


